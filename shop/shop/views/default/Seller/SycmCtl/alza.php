		if(inputLength > limitNum){
			$(this).val($(this).val().slice(0,limitNum))
		}
		$(".short-msg-editor-limit").text(inputLength+"/30");
		
	})
	$(".next-dialog-body .msg-editor-content").keyup(function() {
